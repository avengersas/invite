import asyncio

from . import ALIVE_NAME, eor, legend

menu_category = "fun"


@legend.legend_cmd(
    pattern="ded ([\s\S]*)",
    command=("ded", menu_category),
    info={
        "header": "Just a art command try out yourself to see",
        "usage": "{tr}ded <text>",
    },
)
async def _(event):
    "fun art command"
    name = event.text[4:]
    if name:
        await eor(
            event,
            f"{ALIVE_NAME} --- {name}          \n　　　　　|"
            "\n　　　　　| \n"
            "　　　　　| \n"
            "　　　　　| \n"
            "　　　　　| \n"
            "　　　　　| \n"
            "　　　　　| \n"
            "　　　　　| \n"
            "　／￣￣＼| \n"
            "＜ ´･ 　　 |＼ \n"
            "　|　３　 | 丶＼ \n"
            "＜ 、･　　|　　＼ \n"
            "　＼＿＿／∪ _ ∪) \n"
            "　　　　　 Ｕ Ｕ\n",
        )
    else:
        await eor(event, "Give me some text")


@legend.legend_cmd(
    pattern="killer ([\s\S]*)",
    command=("killer", menu_category),
    info={
        "header": "Just a art command try out yourself to see",
        "usage": "{tr}killer <text>",
    },
)
async def _(event):
    "fun art command"
    name = event.pattern_match.group(1)
    if name:
        await eor(
            event,
            f"__**Commando **__{ALIVE_NAME}          \n\n"
            "_/﹋\_\n"
            "(҂`_´)\n"
            f"<,︻╦╤─ ҉ - - - {name}\n"
            "_/﹋\_\n",
        )
    else:
        await eor("Give me some Text")


